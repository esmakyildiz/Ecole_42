char	*ft_strlowcase(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 'A') && (str[c] <= 'Z'))
		str[c] += 'a' - 'A';
		c++;
	}
	return (str);
}
